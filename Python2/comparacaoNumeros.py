num1 = float(input("Digite um numero: "))
num2 = float(input("Digite mais um nuemro: "))
if num1 > num2:
    print("O primeiro nuemro é maior que  segundo: {} ".format(num1))
elif num1 < num2:
    print("O segundo numero é maior que o primeiro: {}".format(num2))
else :
    print(" Não existe valor maior, os dois são iguais: {}".format(num1 == num2))
                   
